/**
 * Get the derpy version of a word, preserving case
 */
export function getDerpyWord(word: string): string {
  const lower = word.toLowerCase();
  const sub = wordSubstitutions[lower];
  
  if (!sub) return word;
  
  // Preserve original case pattern
  if (word === word.toUpperCase()) {
    return sub.toUpperCase();
  }
  if (word[0] === word[0].toUpperCase()) {
    return sub.charAt(0).toUpperCase() + sub.slice(1);
  }
  return sub;
}

/**
 * Transform text by replacing words with their derpy equivalents
 */
export function derpify(text: string): string {
  // Match words (including contractions) while preserving punctuation and whitespace
  return text.replace(/\b[\w']+\b/g, (word) => getDerpyWord(word));
}

/**
 * Get the total number of word substitutions available
 */
export function getSubstitutionCount(): number {
  return Object.keys(wordSubstitutions).length;
}
